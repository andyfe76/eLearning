<p>Ce reprezinta de fapt E-learning? Uneori este asemanat cu e-training sau e-cursuri.
  Ceea ce presupune ca informatia furnizata in cursurile fata in fata este pur
  si simplu transpusa intr-un format de pagina Web. Acestea au fost intr-adevar
  inceputurile e-learningului dar s-a constatat rapid ca exista o lipsa de interactivitate
  iar participantii sa nu fie atrasi in acumularea informatiilor.<br>
  O definitie generala ar avea urmatorul continut: &quot;Invatarea asincrona
  sau sincrona, condusa prin intermediul Internet-ului, Intranet-ului, Extranet-ului
  sau altor tehnologii bazate pe Internet. E-learning include diferite metodologii
  de livrare care includ continut personalizat, clase virtuale, simulari, forumuri,
  discutii focalizate etc.&quot; </p>
<p>In momentul de fata E-learning presupune: evaluarea pre-curs necesara in vederea
  stabilirii vitezei de parcurgere a cursului si a nivelului potrivit, exercitii
  care conduc la interactiune si dinamizeaza invatarea, intrebari care presupun
  diferite tipuri de raspunsuri in vederea testarii cunostintelor, simulari in
  care se folosesc cunostintele dobandite, discutii on-line cu trainerii care
  pot raspunde unor intrebari ce apar pe masura parcurgerii cursului, timp real
  de acces la diferite tipuri de resurse, evaluari pe parcurs pentru a determina
  nivelul de intelegere si asimilare a materialului parcurs. </p>
